package percabangan;

public class Switch {
    public static void main(String[] args) {
        int makanan = 2;
        switch(makanan){
            case 1:
                System.out.println("Ayam Goreng");
                break;
            case 2:
                System.out.println("Bubur Ayam");
                break;
            case 3:
                System.out.println("Bakso");
                break;
            case 4:
                System.out.println("Ikan Bakar");
        }
    }
}
